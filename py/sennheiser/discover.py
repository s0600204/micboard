def discover():
    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    sock.bind(('', MCAST_PORT))

    with selectors.DefaultSelector() as selector:
        selector.register(sock, selectors.EVENT_READ)
        while True:
            for adapter in ifaddr.get_adapters():
                for addr in adapter.ips:
                    if not addr.is_IPv4 or addr.ip == '127.0.0.1':
                        continue
                    with socket.socket(socket.AF_INET, socket.SOCK_DGRAM) as sock2:
                        sock2.bind((addr.ip, MCAST_PORT + 1))
                        sock2.sendto(DISCOVERY_MSG, (MCAST_GRP, MCAST_PORT))
            
            while selector.select(WAIT_TIMEOUT):
                data, (ip4, port) = sock.recvfrom(1024)
                if port != MCAST_PORT:
                    continue
                process_discovery_packet(ip4, data)
                

